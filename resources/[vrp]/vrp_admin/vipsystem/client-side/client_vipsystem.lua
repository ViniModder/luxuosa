local coordList = {
    vec3(-1195.46,-1778.55,4.63),--spaner 1
    vec3(-1186.58,-1790.54,4.6),---spaner
    vec3(159.75, -1008.01, 29.52),---pra√ßa
}

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- THREAD
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
CreateThread(function()
    while true do
        local SLEEP_TIME = 1000
        local plyCoords = GetEntityCoords(PlayerPedId())
        for k,coords in pairs(coordList) do 
            local dist = #(plyCoords - coords)
            if dist <= 10 then
                SLEEP_TIME = 0
                DrawMarker(25, coords.x,coords.y,coords.z-0.95,0,0,0,0,0,0,1.0,1.0,1.0, 255, 255, 255, 255, 0,0,0,1)

                if dist <= 3 then
                    drawTxt("PRESSIONE  ~r~E~w~  PARA ABRIR A LOJA E APROVEITAR O CUPOM DE 50%: ~p~LUXUOSA50",4,0.5,0.93,0.5, 10, 36, 223, 180)
                    if IsControlJustPressed(0,38) then
                        OpenNuiVip()
                    end
                end
            end
        end 
        Wait( SLEEP_TIME )
    end
end)

